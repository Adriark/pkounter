import { writeFile } from "node:fs/promises";

const API_ROOT = "https://munchstats.com/api";
const DEFAULT_FORMAT = "gen9championsvgc2026regmabo3";

const FORMATS = [
  {
    format: "gen9championsvgc2026regmabo3",
    label: "VGC 2026 Reg M-A (Bo3)",
    mode: "6 → 4 dobles",
    battle: "doubles",
    rating: "1760",
    seed: "Sneasler",
  },
  {
    format: "gen9championsbssregma",
    label: "BSS Reg M-A",
    mode: "6 → 3 singles",
    battle: "singles",
    rating: "1760",
    seed: "Garchomp",
  },
  {
    format: "gen9championsou",
    label: "Champions OU",
    mode: "6v6 singles",
    battle: "singles",
    rating: "1760",
    seed: "Garchomp",
  },
];

const STAT_KEYS = ["hp", "atk", "def", "spa", "spd", "spe"];

function toId(text) {
  return String(text || "").toLowerCase().replace(/[^a-z0-9]+/g, "");
}

function parseSpread(spreadText) {
  const [nature, spread] = String(spreadText || "").split(":");
  const values = String(spread || "").split("/").map((value) => Number(value || 0));
  if (!nature || values.length !== 6 || values.some((value) => Number.isNaN(value))) return null;
  return {
    nature,
    spread: Object.fromEntries(STAT_KEYS.map((key, index) => [key, values[index] || 0])),
  };
}

function compactSpread(entry) {
  const parsed = parseSpread(entry?.[0]);
  if (!parsed) return null;
  return {
    nature: parsed.nature,
    spread: parsed.spread,
    usage: Number(entry?.[1] || 0),
  };
}

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${res.statusText}: ${url}`);
  return res.json();
}

function apiUrl(config, pokemon, month = "") {
  const url = new URL(`${API_ROOT}/${config.format}/${config.rating}/${encodeURIComponent(pokemon)}`);
  if (month) url.searchParams.set("month", month);
  return url.href;
}

function compactPokemon(data) {
  const spread = parseSpread(data.spreads_list?.[0]?.[0]) || {};
  const spreads = (data.spreads_list || []).map(compactSpread).filter(Boolean).slice(0, 8);
  return {
    usage: Number(data.current_pokemon?.[1] || 0),
    rank: Number(data.current_pokemon?.[2] || 0),
    moves: (data.moves_list || []).slice(0, 8).map((entry) => entry[0]),
    moveUsage: Object.fromEntries((data.moves_list || []).map((entry) => [entry[0], Number(entry[1] || 0)])),
    items: (data.items_list || []).slice(0, 6).map((entry) => entry[0]),
    itemUsage: Object.fromEntries((data.items_list || []).map((entry) => [entry[0], Number(entry[1] || 0)])),
    abilities: (data.abilities_list || []).slice(0, 4).map((entry) => entry[0]),
    abilityUsage: Object.fromEntries((data.abilities_list || []).map((entry) => [entry[0], Number(entry[1] || 0)])),
    teammates: (data.teammates_list || []).slice(0, 8).map((entry) => entry[0]),
    natureUsage: Object.fromEntries((data.natures_list || []).map((entry) => [entry[0], Number(entry[1] || 0)])),
    spreads,
    nature: spread.nature || data.natures_list?.[0]?.[0] || "",
    spread: spread.spread || null,
  };
}

async function syncFormat(config) {
  console.log(`Sync ${config.label} (${config.format})...`);
  const seed = await fetchJson(apiUrl(config, config.seed));
  const latestMonth = seed.selected_month || seed.available_months?.at(-1) || "";
  const names = (seed.pokemon_names || []).map((entry) => entry[0]);
  const pokemon = {};

  for (const [index, name] of names.entries()) {
    const data = name === seed.selected_pokemon ? seed : await fetchJson(apiUrl(config, name, latestMonth));
    pokemon[toId(name)] = compactPokemon(data);
    if ((index + 1) % 25 === 0 || index + 1 === names.length) console.log(`  ${config.label}: ${index + 1}/${names.length}`);
  }

  return {
    format: config.format,
    label: config.label,
    mode: config.mode,
    battle: config.battle,
    rating: config.rating,
    month: latestMonth,
    pokemon,
  };
}

const formats = {};
for (const config of FORMATS) {
  formats[config.format] = await syncFormat(config);
}

const defaultStats = formats[DEFAULT_FORMAT] || Object.values(formats)[0];
const output = `// Generated by scripts/sync-munchstats.mjs from MunchStats.\n` +
  `// Formats: ${Object.values(formats).map((format) => `${format.label}, rating ${format.rating}, month ${format.month}`).join(" | ")}.\n` +
  `const MUNCH_STATS = ${JSON.stringify({
    defaultFormat: DEFAULT_FORMAT,
    format: defaultStats.format,
    label: defaultStats.label,
    rating: defaultStats.rating,
    month: defaultStats.month,
    pokemon: defaultStats.pokemon,
    formats,
  }, null, 2)};\n`;

await writeFile("munchstats-data.js", output, "utf8");
console.log(`Wrote munchstats-data.js with ${Object.keys(formats).length} formats.`);
